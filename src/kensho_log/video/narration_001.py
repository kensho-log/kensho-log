TELP_FINALE = "元本は三案で同額。差額は中央に表示。税・手数料・為替は不算入。"
TELP_LIMITS = "限界:後知恵 / 税手数料為替 / 将来は不確実。Voice:VOICEVOX / 四国めたん"


def build_race_telop_lines(num_frames: int) -> list[str]:
    """レース フレーム数分のテロップを 4 ブロック等分で並べる。"""
    n = int(num_frames)
    if n < 1:
        raise ValueError("num_frames must be >= 1")
    segs = [TELP_RACE_1, TELP_RACE_2, TELP_RACE_3, TELP_RACE_4]
    out: list[str] = []
    for i in range(n):
        q = int((i * 4) // n)
        if q > 3:
            q = 3
        out.append(segs[q])
    return out


